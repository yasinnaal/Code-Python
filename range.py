
#range(start, stop, step_size)
# To force range function to output all the items, we use the function list()

print(range(10)) # without calling list

print(list(range(10))) #calling list

print(list(range(2, 10))) # start from 2

print(list(range(2, 10, 1))) # start from 2, stop at 10, step 1